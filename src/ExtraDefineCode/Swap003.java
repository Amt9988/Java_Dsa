package ExtraDefineCode;

public class Swap003 {
    public static void main(String[] args) {
        //java swap programme 003
        int temp;
        int a=20;
        int b=40;

        temp=a;
        a=b;
        b=temp;
//
//        temp=b;
//        b=a;
//        a=temp;
        System.out.println("After Swaping number a is:-"+a); //a=20
        System.out.println("After Swaping number b is:-"+b);    //b=40
        System.out.println(temp);
    }
}
